  name: 'customer_statistics',
  data () {
    return {
      imgUrl_loading:false,
      search: '', // 关键字
      customer_stylist: '', // 设计师
      stage_name: '', // 阶段
      listName: '',
      osd:true,
      optioos:'',
      list: [{
        customer_name:'',
        customer_stylist:'',
        customer_budget:'',
        stage_name:'',
        stage_startdate:'',
        stage_stipulate:'',
        customer_id:''
      }], // table
      add: '', // 钱总
      listtime: '', // 倒计时
      /* table */
      ostyle: {
        fontSize: '12px',
        borderBottom: '1px solid #dadada',
        lineHeight: '30px'
      },
      /* table的最后一个td */
